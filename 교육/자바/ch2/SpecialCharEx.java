package ch2;

class SpecialCharEx {
	public static void main(String[] args) {
		System.out.println('\'');		// ''' 대신 '''를 출력.
		System.out.println("abc\t123\b456");	// 탭과 백스페이스
		System.out.println('\n');		// 개행문자 출력하고 개행
		System.out.println("\"Hello\"");	// 큰따옴표 출력
		System.out.println("c:\\");		// 역슬래쉬 출력
	}
}
