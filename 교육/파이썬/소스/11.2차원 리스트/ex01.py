list = [
    [10,20,30], # 0행
    [40,50,60], # 1행
   #0열 1열 2열
]
# 2행 3열 2차원 리스트
print(list)
print(list[0][0],end=' ') #0행 0열 - 10
print(list[0][1],end=' ') #0행 1열 - 20
print(list[0][2])         #0행 2열 - 30
print(list[1][0],end=' ') #1행 0열 - 40
print(list[1][1],end=' ') #1행 1열 - 50
print(list[1][2])         #1행 2열 - 60