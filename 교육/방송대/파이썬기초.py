#>	문제 3, 챗지피티
# 문제 3
weight = float(input("몸무게(kg)를 입력하세요: "))
height = float(input("키(m)를 입력하세요: "))

bmi = weight / (height ** 2)
print("당신의 BMI는 {:.2f}입니다.".format(bmi))

if bmi < 18.5:
    status = "저체중"
elif bmi < 23.0:
    status = "정상"
elif bmi < 25.0:
    status = "과체중"
elif bmi < 30.0:
    status = "비만"
else:
    status = "고도비만"

print("판정 결과:", status)


